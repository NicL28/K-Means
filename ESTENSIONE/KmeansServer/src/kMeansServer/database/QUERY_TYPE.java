package kMeansServer.database;

/**
 * Enumerazione che rappresenta i tipi di query supportati.
 * MIN: restituisce il valore minimo.
 * MAX: restituisce il valore massimo.
 */
public enum QUERY_TYPE {
    /**
     * Valore MIN in una query.
     */
    MIN,
    /**
     * Valore MAX in una query.
     */
    MAX
}
